package org.Inheritance;

public class Single {
       public void mother() {
    	   System.out.println("priya");
       }

       public void sister() {
    	   System.out.println("sindhu");
       }
       public static void main(String[]args) {
    	   Single o = new Single();
    	   
    	   o.mother();
    	   o.sister();
    	   
       }
}
